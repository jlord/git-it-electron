my(%u2e, $prim, $wt);
while (<DATA>) {
    last if /^__END__/;
    my @c = split;
    if (@c == 1 && $c[0] =~ /^[A-D]/) {
	my $u = hex $c[0];
	my @h = Unicode::Collate::_decompHangul($u);
	$prim = [ map $jamo2prim{$_}, @h ];
	$wt = 0x20;
    } else {
	for my $c (@c) {
	    next if !$c;
	    $wt++;
	    $u2e{hex($c)} = [ $wt, @$prim ];
	}
    }
}

sub weightKorean {
    my $u = shift;
    return undef if !exists $u2e{$u};
    my @a = @{ $u2e{$u} };
    my $s = shift @a;
    my $p = shift @a;
    return([ $p, $s, 0x2, $u ], @a);
}

